
function describeCountry(country, population, capitalCity){
    
  var description = country + ' has ' + population + " million people and it's capital city is " + capitalCity;
  return description;
}
console.log(describeCountry("Finland", 6, "helsinki"));
console.log(describeCountry("China", 1.441, "Bac Kinh"));
console.log(describeCountry("VietNam", 97, "Ha Noi"))